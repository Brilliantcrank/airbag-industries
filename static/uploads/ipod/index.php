<H3>Sing As Pretty As A Song Bird </H3>
<P>Unlike other members of the iPod family, iPod Ashlee Simpson Karaoke Edition makes 
lip syncing to music sheer delight. Taking a cue from our one-button computer mice, we have eliminated the confusion of five buttons and trimmed it down to a fast-forward. Just click the large button and you're on to the next song! And you 
      can certainly keep plenty of your music on this 20GB iPod. In fact, after 
      purchasing and downloading "The Complete Ashlee Simpson," you'll still have enough room for 
    as many as 4,996 other songs.<SUP>(1)</SUP></P>
<IMG class=right height=200 
alt="Thinking of better days" src="ipod_files/indexposterse10262004.jpg" width=150 
vspace=3 border=0> 
<P>No matter how much music or how many Audiobooks (you still read right?) you bring with you, however, 
iPod Karaoke Edition remains small and light enough to carry in your pocket: 
it's no bigger than a pack of playing cards and weighs just 5.6 ounces. No one will ever see an unsightly bulge when you use the iPod Karaoke Edition to &quot;sing&quot; &quot;live&quot; again! </P>
<P>It also 
      offers up to 12 hours of battery life, so you can sync to music practically 
    all day, after all you're not really using your voice at all so why not?.<SUP>(2) </SUP>And thanks to 25 minutes of skip protection, you can do a hoedown just like Ashlee without skipping like Milli Vanilli.</P>
<P>What's more, with iPod Karaoke Edition, you enjoy the best <a "http://www.apple.com/ipod/u2/autosync.html">auto-sync</A> in the industry *wink*. 
Just connect iPod to your Mac, PC or a Sony My First Computer, and iTunes takes care of business, keeping 
both iTunes and iPod in perfect harmony. Harmony? Webster's says it's &quot;simultaneous combination of notes in a chord&quot;. Don't fret, we had to look it up too. </P>
