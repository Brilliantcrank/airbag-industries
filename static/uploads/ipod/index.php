<H3>Never Miss A Beat </H3>
<P>Like no other, iPod Ashlee Simpson Karaoke Edition stands out. Virgin white, it features the new Apple Fast-Forward Click Button and, on the flip side, complete how to use instructions. Available for just $349, it comes with enough money to use a pay phone for calling your daddy, your agent or both, when you get caught lip syncing live on national television. We have also included some Kleenex for those special times when your entire fan base realizes you're nothing but a talentless hack.</P>
